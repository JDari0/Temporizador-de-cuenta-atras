    private void txt_hKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_hKeyPressed

        if (evt.isControlDown()) {
            evt.consume(); //Cancela la tecla precionada
            Toolkit.getDefaultToolkit().beep(); //Sonido de mensaje de error  

            JOptionPane.showMessageDialog(null, "Control deshabilitado\n Ingrese por teclado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txt_hKeyPressed

