    private Timer tiempo;
    private final ActionListener tarea = new ActionListener() {

        @Override
        public void actionPerformed(ActionEvent ae) {

            txt_h.setEditable(false);
            txt_m.setEditable(false);
            txt_s.setEditable(false);

            int h = Integer.parseInt(txt_h.getText());
            int m = Integer.parseInt(txt_m.getText());
            int s = Integer.parseInt(txt_s.getText());

            s--;
            System.out.println("Secs: " + s);

            if (s <= -1) {
                m--;
                System.out.println("Mins: " + m);
                s = 59;
            }

            if (m < 0) {
                h--;
                System.out.println("Hrs: " + h);
                m = 59;
            }

            if (h < 0) {
                tiempo.stop();
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(null, "Tiempo Finalizado");

                txt_h.setText("");
                txt_m.setText("");
                txt_s.setText("");

                txt_h.setEditable(true);
                txt_m.setEditable(true);
                txt_s.setEditable(true);

                txt_h.requestFocus();

                btn_iniciar.setEnabled(true);
                btn_iniciar.setText("Iniciar");

                btn_pausar.setEnabled(false);
                btn_detener.setEnabled(false);

                lbl_h1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));
                lbl_h2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));

                lbl_m1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));
                lbl_m2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));

                lbl_s1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));
                lbl_s2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + 0 + ".png")));

                return;
            }

            int s1 = s / 10; // Si 2 números enteros se dividen la division se hace como números enteros por lo q queda sin decimales  
            System.out.println("s1: " + s1);

            int s2 = s % 10;
            System.out.println("s2: " + s2);

            int m1 = m / 10;
            int m2 = m % 10;

            int h1 = h / 10;
            int h2 = h % 10;

            lbl_h1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + h1 + ".png")));
            lbl_h2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + h2 + ".png")));

            lbl_m1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + m1 + ".png")));
            lbl_m2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + m2 + ".png")));

            lbl_s1.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + s1 + ".png")));
            lbl_s2.setIcon(new ImageIcon(getClass().getResource("/Iconos" + "/" + (s2 < 0 ? "0" : s2) + ".png")));

            txt_h.setText(h + "");
            txt_m.setText(m + "");
            txt_s.setText(s + "");
        }
    };

